public class Task3 {
    public static void boolOper(boolean flag1,boolean flag2){
        System.out.println("|| ="+(flag1||flag2));
        System.out.println("&& ="+(flag1&&flag2));
        System.out.println("!flag1 ="+!(flag1));
        System.out.println("!flag2 ="+!(flag2));
        System.out.println("^ ="+(flag1^flag2));
    }
}
